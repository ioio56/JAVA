package ch02_variable;

public class VariableType {
	// 변수의 타입(기본타입)
	
		//정수 타입
		byte byteVar = 1;
		short shortVar = 2;
		int intVar = 3;
		long longVar = 4l; //Long 타입은 숫자 뒤에 l붙여야 한
		long longVar2 = 4L;
		
		//정수이면서 문자에 해당
		char charVar = 44032; // '가'와 같다.
		char ga ='가'; //44032와 같아
		
		// 소수 타입
		double doubleVar = 3.14; //용량이 큼
		float floatVar = 3.14f; //용량이 적고 f를 붙임
		
		// 불리언 타입(참/거짓, true/false)
		boolean boolVar = false;
		
		// 문자열 타입 (문자, char는 한글자/ 문자열은 0글자 이상)
		String strToday = "오늘은 3월 21일"; //글자수 제한 없다고 봐도 무방
		
}
